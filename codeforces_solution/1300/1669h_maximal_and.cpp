const ld EPS = 1e-9;


void solve() {
    ll n, k;
    cin >> n >> k;
    vector<ll>cnt(31, 0);
    ll x;
    for(ll i = 0; i < n; ++i) {
        cin >> x;
        for(int j = 30; j >= 0; --j) { 
            if(x & (1 << j)) cnt[j]++;
        }
    }
    ll ans = 0;
    for(ll j = 30; j >= 0; --j){
        ll need = n  - cnt[j];
        if(need <= k) {
            k -= need;
            ans += (1 << j);
        }
    }
    cout << ans << endl;
}    

int main() {
    double start = clock();
    int tc = 1;
    cin >> tc;
    while(tc--) {
       solve();
    }
    cerr << "[*] Time: " << (clock() * 1.0 - start) / CLOCKS_PER_SEC << std::setprecision(3) << " s" << endl;
    return 0;
}
